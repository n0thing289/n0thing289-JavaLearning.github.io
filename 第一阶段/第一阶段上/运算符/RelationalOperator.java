public class RelationalOperator {

	public static void main(String[] args) {
		//演示关系运算符的使用
		int a = 9;
		int b = 8;
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a <= b);
		System.out.println(a < b);
		System.out.println(a == b);
		System.out.println(a != b);
		boolean flag = a > b;//true//关系表达式
		System.out.println("flag= " + flag);
	}
}