public class Char01 {
	public static void main(String[] args) {
		char c1 = 'a';
		char c2 = '\t';//  \t整体是一个字符
		char c3 = '韩';//可以存放一个汉字
		char c4 = 97;//注意输出的不是97
		System.out.println(c1);//ctrl + shift + d 复制光标所在行到下一行
		System.out.println(c2);
		System.out.println(c3);
		System.out.println(c4);//当输出c4时，会输出97表示的字符
	


	}
}